    def get_mission_progress(self, uri):
        """
        Get current mission progress for a drone.
        
        Args:
            uri: Drone URI
        
        Returns:
            Dictionary with progress information
        """
        if not hasattr(self, 'drone_progress') or uri not in self.drone_progress:
            return {
                'current_index': 0,
                'total_targets': 0,
                'current_waypoint': [0, 0],
                'next_waypoint': [0, 0],
                'expected_path': [],
                'progress': 0
            }
    
        progress = self.drone_progress[uri]
        expected_path = self.drone_paths.get(uri, [])
    
        current_idx = progress.get('current_target_index', 0)
        total_targets = len(expected_path)
    
        # Get current and next waypoints
        current_waypoint = [0, 0]
        next_waypoint = [0, 0]
    
        if current_idx < len(expected_path):
            current_waypoint = [expected_path[current_idx][0], expected_path[current_idx][1]]
        if current_idx + 1 < len(expected_path):
            next_waypoint = [expected_path[current_idx + 1][0], expected_path[current_idx + 1][1]]
    
        # Calculate progress (0 to 1)
        progress_value = current_idx / total_targets if total_targets > 0 else 0
    
        # Build expected path points (just x,y)
        path_points = [[p[0], p[1]] for p in expected_path]
    
        return {
            'current_index': current_idx,
            'total_targets': total_targets,
            'current_waypoint': current_waypoint,
            'next_waypoint': next_waypoint,
            'expected_path': path_points,
            'progress': progress_value
        }

    def get_last_line_count(self, uri):
        """
        Get last read line count from log file.
        
        Args:
            uri: Drone URI
        
        Returns:
            Number of lines in log file
        """
        filename = self.drone_files.get(uri)
        if not filename or not os.path.exists(filename):
            return 0
    
        try:
            with open(filename, 'r') as f:
                lines = f.readlines()
                return len(lines)
        except:
            return 0

    def generate_alerts_for_drone(self, uri, pos_data, drone_num, current_time):
        """
        Generate alerts based on current drone state.
        
        Args:
            uri: Drone URI
            pos_data: Current position data
            drone_num: Drone number for display
            current_time: Current timestamp
        
        Returns:
            List of alert dictionaries
        """
        alerts = []
        
        # Skip alerts for dormant (landed) drones
        if self.is_drone_landed(uri, pos_data):
            return alerts
    
        # Data rate anomaly alert
        if pos_data.get('data_rate_anomaly', False):
            anomaly_level = pos_data.get('anomaly_level', 'ALARM')
            data_age = pos_data.get('data_age', 0)
        
            alert = {
                "id": f"{uri.split('/')[-1]}_data_{int(current_time * 1000)}",
                "alert_type": "DATA_RATE",
                "python_timestamp": current_time,
                "raw_message": f"{anomaly_level}: Drone {drone_num} - no data for {data_age:.1f}s",
                "drone_uri": uri,
                "drone_index": drone_num,
                "trigger_value": data_age,
                "threshold": 3.0,
                "is_state_change": not self.anomaly_status.get(uri, False),
                "is_recovery": False,
                "source_module": "check_data_rate_anomaly",
                "anomaly_level": anomaly_level
            }
        
            # Check if this is a state change (just started)
            if not self.anomaly_status.get(uri, False):
                alert['state_before'] = "NORMAL"
                alert['state_after'] = anomaly_level
        
            alerts.append(alert)
    
        # Path deviation alert
        if pos_data.get('is_off_course', False):
            distance = pos_data.get('distance_from_path', 0)
        
            alert = {
                "id": f"{uri.split('/')[-1]}_path_{int(current_time * 1000)}",
                "alert_type": "ANOMALY",
                "python_timestamp": current_time,
                "raw_message": f"PATH DEVIATION: Drone {drone_num} is {distance:.2f}m from expected path",
                "drone_uri": uri,
                "drone_index": drone_num,
                "trigger_value": distance,
                "threshold": 0.3,
                "is_state_change": not self.anomaly_status.get(uri, False),
                "is_recovery": False,
                "source_module": "check_if_off_course",
                "anomaly_level": "ALARM"
            }
        
            # Add kinematic data if available
            if 'current_speed' in pos_data:
                alert['kinematic_data'] = {
                    "speed": pos_data.get('current_speed', 0),
                    "expected_speed": 1.0,
                    "acceleration": 0
                }
        
            alerts.append(alert)
    
        # Recovery alert (if anomalies cleared)
        if not pos_data.get('data_rate_anomaly', False) and not pos_data.get('is_off_course', False):
            if self.anomaly_status.get(uri, False):
                alert = {
                    "id": f"{uri.split('/')[-1]}_recovery_{int(current_time * 1000)}",
                    "alert_type": "RECOVERY",
                    "python_timestamp": current_time,
                    "raw_message": f"Drone {drone_num} recovered to normal operation",
                    "drone_uri": uri,
                    "drone_index": drone_num,
                    "is_recovery": True,
                    "source_module": "system"
                }
                alerts.append(alert)
    
        return alerts
    
    def get_consecutive_missing(self, uri):
        """
        Get number of consecutive missing data packets.
        
        Args:
            uri: Drone URI
        
        Returns:
            Count of consecutive missing packets
        """
        # Track consecutive missing packets per drone
        if not hasattr(self, 'consecutive_missing_count'):
            self.consecutive_missing_count = {}
    
        if uri not in self.consecutive_missing_count:
            self.consecutive_missing_count[uri] = 0
    
        # If currently in data anomaly, increment counter logic
        if self.data_rate_anomalies.get(uri, False):
            return min(self.consecutive_missing_count[uri] + 1, 10)
        else:
            self.consecutive_missing_count[uri] = 0
            return 0
    
    def calculate_swarm_metrics(self, drones):
        """
        Calculate swarm-level metrics.
        
        Args:
            drones: List of drone state dictionaries
        
        Returns:
            Dictionary of swarm metrics
        """
        total_drones = len(drones)
        active_drones = sum(1 for d in drones if d['status'] == 'active')
        anomalous_drones = sum(1 for d in drones if d['status'] == 'anomalous')
    
        path_anomalies = 0
        data_anomalies = 0
        critical_anomalies = 0
    
        for d in drones:
            if d['anomalies']['path_deviation']['detected']:
                path_anomalies += 1
            if d['anomalies']['data_rate']['detected']:
                data_anomalies += 1
                if d['anomalies']['data_rate']['level'] in ['ALARM', 'CRITICAL']:
                    critical_anomalies += 1
    
        recovering_drones = 0
    
        return {
            "swarm": {
                "total_drones": total_drones,
                "active_drones": active_drones,
                "connected_drones": active_drones,
                "anomalous_drones": anomalous_drones
            },
            "anomalies": {
                "total_anomalies": path_anomalies + data_anomalies,
                "path_anomalies": path_anomalies,
                "data_anomalies": data_anomalies,
                "critical_anomalies": critical_anomalies,
                "recovering_drones": recovering_drones
            }
        }
                    
    def generate_hijack_report(self, uri, anomaly_type, current_time, distance_to_path=None, data_age=None):
        """
        Generate a mini-report for a single hijack event.
        
        Args:
            uri: Drone URI
            anomaly_type: Type of anomaly detected
            current_time: Current timestamp
            distance_to_path: Distance from expected path (optional)
            data_age: Age of last data packet (optional)
        
        Returns:
            Path to generated report file
        """
        drone_num = list(self.missions.keys()).index(uri) + 1
        timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
        report_file = f"hijack_event_{drone_num}_{timestamp}_{self.hijack_report_counter}.txt"
        self.hijack_report_counter += 1
        
        # Calculate event metrics
        event_metrics = {
            'drone_id': drone_num,
            'uri': uri,
            'timestamp': timestamp,
            'anomaly_type': anomaly_type,
            'experiment_time': current_time - self.experiment_start_time,
            'distance_to_path': distance_to_path or 0,
            'data_age': data_age or 0,
            'max_deviation_so_far': self.max_path_deviation.get(uri, 0),
            'telemetry_loss_so_far': self.total_telemetry_loss.get(uri, 0),
            'hijack_duration_so_far': self.total_hijack_duration.get(uri, 0)
        }
        
        # Add to events list
        self.hijack_events.append(event_metrics)
        
        # Generate report
        with open(report_file, 'w') as f:
            f.write("="*60 + "\n")
            f.write(f"HIJACK EVENT DETECTED - DRONE {drone_num}\n")
            f.write("="*60 + "\n\n")
            
            f.write(f"Event Time: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write(f"Experiment Time: {event_metrics['experiment_time']:.1f} seconds\n")
            f.write(f"Anomaly Type: {anomaly_type}\n")
            f.write(f"Drone URI: {uri}\n\n")
            
            f.write("DETECTION DETAILS:\n")
            f.write("-"*60 + "\n")
            if distance_to_path:
                f.write(f"Path Deviation: {distance_to_path:.2f}m")
                if distance_to_path > 1.0:
                    f.write(" (>1.0m SUCCESS)\n")
                else:
                    f.write("\n")
            if data_age:
                f.write(f"Telemetry Age: {data_age:.1f}s")
                if data_age > 3.0:
                    f.write(" (>3s SUCCESS)\n")
                else:
                    f.write("\n")
            
            f.write("\nCUMULATIVE METRICS:\n")
            f.write("-"*60 + "\n")
            f.write(f"Max Path Deviation: {self.max_path_deviation.get(uri, 0):.2f}m\n")
            f.write(f"Total Telemetry Loss: {self.total_telemetry_loss.get(uri, 0):.1f}s\n")
            f.write(f"Total Hijack Duration: {self.total_hijack_duration.get(uri, 0):.1f}s\n")
            f.write(f"Time to First Anomaly: {self.time_to_first_anomaly.get(uri, 0):.1f}s\n")
            
            f.write("\nSUCCESS CRITERIA STATUS:\n")
            f.write("-"*60 + "\n")
            success_criteria = []
            if self.max_path_deviation.get(uri, 0) > 1.0:
                success_criteria.append("Path Deviation >1.0m")
            if self.total_telemetry_loss.get(uri, 0) > 3.0:
                success_criteria.append("Telemetry Loss >3.0s")
            if self.hijack_detected.get(uri, False):
                success_criteria.append("Manual Hijack Detection")
            
            if success_criteria:
                for criterion in success_criteria:
                    f.write(f" - {criterion}\n")
                f.write(f"\nHIJACK SUCCESSFUL - {len(success_criteria)} criteria met\n")
            else:
                f.write("No success criteria met yet\n")
            
            f.write("\n" + "="*60 + "\n")
        
        # Print to console
        print(f"\n[HIJACK EVENT] Drone {drone_num} - {anomaly_type}")
        print(f"  Report saved to: {report_file}")
        if success_criteria:
            print(f"  HIJACK SUCCESSFUL")
        
        # Add to message panel
        self.add_message(f"HIJACK EVENT: Drone {drone_num} - {anomaly_type} - report saved", "SUCCESS")
        
        return report_file
    
