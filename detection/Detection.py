    def distance_to_line_segment(self, px, py, p1, p2):
        """
        Calculate minimum distance from point to line segment.
        
        Args:
            px, py: Point coordinates
            p1, p2: Line segment endpoints
        
        Returns:
            Minimum distance
        """
        x1, y1 = p1
        x2, y2 = p2
    
        dx = x2 - x1
        dy = y2 - y1
    
        # If segment is a point
        if dx == 0 and dy == 0:
            return np.sqrt((px - x1)**2 + (py - y1)**2)
    
        # Project point onto line
        t = ((px - x1) * dx + (py - y1) * dy) / (dx * dx + dy * dy)
    
        # Clamp t to segment range [0, 1]
        if t < 0:
            # Closest to p1
            return np.sqrt((px - x1)**2 + (py - y1)**2)
        elif t > 1:
            # Closest to p2
            return np.sqrt((px - x2)**2 + (py - y2)**2)
        else:
            # Closest point is on the segment
            proj_x = x1 + t * dx
            proj_y = y1 + t * dy
            return np.sqrt((px - proj_x)**2 + (py - proj_y)**2)

    def check_if_off_course(self, uri, x, y):
        """
        Check if drone is far from its expected path between waypoints.
        
        Args:
            uri: Drone URI
            x, y: Current position
        
        Returns:
            Tuple of (off_course, distance_from_path)
        """
        if uri not in self.drone_paths:
            return False, 0

        expected_path = self.drone_paths[uri]

        if not expected_path:
            return False, 0

        # Use time since first data for this drone instead of global experiment time
        if not hasattr(self, 'first_data_time') or uri not in self.first_data_time:
            # Fall back to global experiment time if first data time not recorded
            experiment_time = time.time() - self.experiment_start_time
        else:
            experiment_time = time.time() - self.first_data_time[uri]
            if experiment_time < 0:
                experiment_time = 0
    
        # Initialize takeoff tolerance tracking - 25 seconds from first data
        if not hasattr(self, 'takeoff_end_time_experiment'):
            self.takeoff_end_time_experiment = {}
    
        if uri not in self.takeoff_end_time_experiment:
            # Set takeoff tolerance to 25 seconds into the mission (from first data)
            self.takeoff_end_time_experiment[uri] = 25.0
            drone_num = list(self.missions.keys()).index(uri) + 1
            self.add_message(f"Drone {drone_num} - takeoff tolerance until {self.takeoff_end_time_experiment[uri]:.1f}s after first data", "SYSTEM")
    
        # Check if we're still in takeoff tolerance period
        is_takeoff_phase = experiment_time < self.takeoff_end_time_experiment[uri]

        if not hasattr(self, 'drone_progress'):
            self.drone_progress = {}

        if uri not in self.drone_progress:
            self.drone_progress[uri] = {
                'current_target_index': 1,
                'last_position': None,
                'last_update_time': experiment_time,
                'last_passed_index': 0,
                'delivery_phase_until': 0
            }

        progress = self.drone_progress[uri]
        current_target_index = progress['current_target_index']

        if current_target_index >= len(expected_path):
            return False, 0

        target_point = expected_path[current_target_index]
        target_x, target_y = target_point[0], target_point[1]

        previous_index = progress['last_passed_index']
        prev_point = expected_path[previous_index]
        prev_x, prev_y = prev_point[0], prev_point[1]

        distance_to_path_segment = self.distance_to_line_segment(x, y, (prev_x, prev_y), (target_x, target_y))
        distance_to_target = np.sqrt((x - target_x)**2 + (y - target_y)**2)

        reached_target = distance_to_target < 0.3

        if reached_target:
            progress['last_passed_index'] = current_target_index
            progress['current_target_index'] += 1
            progress['delivery_phase_until'] = experiment_time + 20.0
            drone_num = list(self.missions.keys()).index(uri) + 1
            self.add_message(f"Drone {drone_num} reached waypoint {current_target_index} - delivery phase for 20s", "CONTROLLER")
            return False, 0

        # Check if we're in delivery phase (descent/ascent)
        is_delivery_phase = experiment_time < progress.get('delivery_phase_until', 0)

        # During takeoff phase, never flag
        if is_takeoff_phase:
            off_course = False
            if distance_to_path_segment > self.max_path_deviation.get(uri, 0):
                self.max_path_deviation[uri] = distance_to_path_segment
        # During delivery phase, use higher threshold (allow descent/ascent deviations)
        elif is_delivery_phase:
            # Higher threshold during delivery (0.5m instead of 0.3m)
            off_course = distance_to_path_segment > 0.5
        else:
            # Normal flight - standard threshold
            off_course = distance_to_path_segment > 0.3

        progress['last_position'] = (x, y)
        progress['last_update_time'] = experiment_time

        if off_course and not is_takeoff_phase:
            drone_num = list(self.missions.keys()).index(uri) + 1
            threshold_str = "0.5m" if is_delivery_phase else "0.3m"
            self.add_message(f"HIJACK SUCCESS: Drone {drone_num} deviated {distance_to_path_segment:.2f}m from expected path (> {threshold_str})", "SUCCESS")

        return off_course, distance_to_path_segment

    def read_latest_positions(self):
        """
        Read the latest positions from all drone log files.
        
        Returns:
            Dictionary mapping URIs to latest position data
        """
        new_positions = {}
        current_time = time.time()
    
        # Track when we first see data for each drone (for takeoff tolerance)
        if not hasattr(self, 'first_data_time'):
            self.first_data_time = {}
    
        for uri, filename in self.drone_files.items():
            if os.path.exists(filename):
                try:
                    found_new_data = False
                    latest_data_timestamp = None
            
                    with open(filename, 'r') as f:
                        lines = f.readlines()
            
                    for line in reversed(lines):
                        line = line.strip()
                        if line and not line.startswith('#') and 'event' not in line:
                            try:
                                data = json.loads(line)
                                if 'x' in data and 'y' in data and 'z' in data:
                                    found_new_data = True
                                    data_timestamp = data.get('timestamp', current_time)
                                    latest_data_timestamp = data_timestamp
                            
                                    # Record when we first see data for this drone
                                    if uri not in self.first_data_time:
                                        self.first_data_time[uri] = data_timestamp
                                        self.add_message(f"Drone {list(self.missions.keys()).index(uri) + 1} first data recorded", "SYSTEM")
                            
                                    data_rate_anomaly, time_since_data, anomaly_level = self.check_data_rate_anomaly(
                                        uri, current_time, has_new_data=True, data_timestamp=data_timestamp)
                            
                                    new_positions[uri] = {
                                        'x': data['x'],
                                        'y': data['y'], 
                                        'z': data['z'],
                                        'timestamp': data_timestamp,
                                        'controller': data.get('controller', 'unknown'),
                                        'datetime': data.get('datetime', ''),
                                        'data_rate_anomaly': data_rate_anomaly,
                                        'time_since_data': time_since_data,
                                        'anomaly_level': anomaly_level,
                                        'data_age': current_time - data_timestamp,
                                        'is_off_course': False,
                                        'distance_from_path': 0,
                                        'current_speed': 0,
                                        'speed_violation': False
                                    }
                                    break
                            except json.JSONDecodeError:
                                continue
            
                    if not found_new_data:
                        data_rate_anomaly, time_since_data, anomaly_level = self.check_data_rate_anomaly(
                            uri, current_time, has_new_data=False)
                
                        if uri in self.drone_positions and self.drone_positions[uri]:
                            last_pos = self.drone_positions[uri][-1]
                            new_positions[uri] = {
                                'x': last_pos[0], 'y': last_pos[1], 'z': 0,
                                'timestamp': current_time,
                                'controller': 'unknown',
                                'data_rate_anomaly': data_rate_anomaly,
                                'time_since_data': time_since_data,
                                'anomaly_level': anomaly_level,
                                'stale_data': True,
                                'data_age': time_since_data
                            }
                        else:
                            drone_num = list(self.missions.keys()).index(uri) + 1
                            message = f"NO DATA: Drone {drone_num} - no position data"
                            self.add_message(message, "DATA_RATE")
        
                except Exception as e:
                    error_msg = f"Error reading {filename}: {e}"
                    self.add_message(error_msg, "SYSTEM")
    
        return new_positions
    
    def update_controller_history(self, uri, controller, is_anomaly=False, is_data_anomaly=False):
        """
        Update controller history and detect changes.
        
        Args:
            uri: Drone URI
            controller: Controller name
            is_anomaly: Whether path anomaly is present
            is_data_anomaly: Whether data anomaly is present
        """
        if uri not in self.controller_history:
            self.controller_history[uri] = []
        
        current_time = time.time()
        current_anomaly = is_anomaly or is_data_anomaly
        
        if current_anomaly != self.anomaly_status.get(uri, False):
            if current_anomaly:
                self.anomaly_start_time[uri] = current_time
                drone_num = list(self.missions.keys()).index(uri) + 1
                if is_data_anomaly:
                    self.add_message(f"DATA ANOMALY: Drone {drone_num} comms lost", "DATA_RATE")
                else:
                    self.add_message(f"PATH ANOMALY: Drone {drone_num} off-course", "ANOMALY")
            else:
                anomaly_duration = current_time - self.anomaly_start_time.get(uri, current_time)
                drone_num = list(self.missions.keys()).index(uri) + 1
                self.add_message(f"RECOVERED: Drone {drone_num} back normal {anomaly_duration:.1f}s", "RECOVERY")
            
            self.anomaly_status[uri] = current_anomaly
        
        if (not self.controller_history[uri] or 
            self.controller_history[uri][-1]['controller'] != controller or
            self.controller_history[uri][-1]['anomaly'] != current_anomaly or
            current_time - self.controller_history[uri][-1]['timestamp'] > 5):
            
            self.controller_history[uri].append({
                'controller': controller,
                'anomaly': current_anomaly,
                'path_anomaly': is_anomaly,
                'data_anomaly': is_data_anomaly,
                'timestamp': current_time,
                'time_str': datetime.now().strftime('%H:%M:%S')
            })
            
            if len(self.controller_history[uri]) > 10:
                self.controller_history[uri] = self.controller_history[uri][-10:]
            
            drone_num = list(self.missions.keys()).index(uri) + 1
            status_parts = []
            if is_anomaly:
                status_parts.append("PATH_ISSUE")
            if is_data_anomaly:
                status_parts.append("DATA_ISSUE")
            status = " | ".join(status_parts) if status_parts else "NORMAL"
            self.add_message(f"Drone {drone_num}: {controller.upper()} | {status}", "CONTROLLER")
    
    def is_drone_landed(self, uri, pos_data):
        """
        Check if drone has landed (mission complete and low altitude).
        
        Args:
            uri: Drone URI
            pos_data: Current position data
        
        Returns:
            True if drone is landed/dormant, False otherwise
        """
        # Get mission progress
        progress = self.get_mission_progress(uri)
        mission_complete = progress.get('progress', 0) >= 1.0
    
        # Check altitude - landed if z < 0.1m (close to ground)
        z_height = pos_data.get('z', 1.0)
        is_landed = z_height < 0.1
    
        # Also check if data is very old (5+ seconds without update)
        data_age = pos_data.get('data_age', 0)
        is_inactive = data_age > 5.0
    
        # Also check if drone has been stationary for a while
        if uri in self.drone_positions and len(self.drone_positions[uri]) > 5:
            recent_positions = self.drone_positions[uri][-5:]
            if len(recent_positions) >= 3:
                x_positions = [p[0] for p in recent_positions]
                y_positions = [p[1] for p in recent_positions]
                x_range = max(x_positions) - min(x_positions)
                y_range = max(y_positions) - min(y_positions)
                is_stationary = x_range < 0.05 and y_range < 0.05
            else:
                is_stationary = False
        else:
            is_stationary = False
    
        # Drone is dormant if mission complete AND landed, OR stationary and inactive
        return (mission_complete and is_landed) or (is_stationary and is_inactive)
    
    def update_plot(self):
        """
        Update the plot with latest positions from log files.
        
        Returns:
            Tuple of (active_count, controllers, path_anomalies, data_anomalies, hijack_count)
        """
        # Remove old drone markers
        for artist in self.ax.collections[:]:
            if hasattr(artist, '_drone_marker'):
                artist.remove()
        
        latest_positions = self.read_latest_positions()
        
        active_drones = 0
        current_controllers = {}
        anomaly_drones = 0
        data_anomaly_drones = 0
        hijack_drones = 0
        
        for uri, pos_data in latest_positions.items():
            if pos_data:
                active_drones += 1
                x, y, z = pos_data['x'], pos_data['y'], pos_data['z']
                controller = pos_data.get('controller', 'unknown')
                data_rate_anomaly = pos_data.get('data_rate_anomaly', False)
                data_age = pos_data.get('data_age', 0)
                current_controllers[uri] = controller
                
                # Check if drone is off-course
                is_off_course, distance_from_path = self.check_if_off_course(uri, x, y)
                
                # Update pos_data so alerts can access these values
                pos_data['is_off_course'] = is_off_course
                pos_data['distance_from_path'] = distance_from_path
                
                # Check if drone is dormant for THIS drone
                is_dormant = self.is_drone_landed(uri, pos_data)
                
                # Detect hijack based on success criteria (only if not dormant)
                hijack_detected = False
                anomaly_type = "NONE"
                current_time = time.time()
                
                if not is_dormant:
                    # Success criteria
                    if data_age > 3.0:
                        hijack_detected = True
                        anomaly_type = "TELEMETRY_LOSS"
                    elif distance_from_path > 0.3:
                        hijack_detected = True
                        anomaly_type = "PATH_DEVIATION"
                    elif data_rate_anomaly and pos_data.get('anomaly_level') in ["ALARM", "CRITICAL"]:
                        hijack_detected = True
                        anomaly_type = "HIJACK_DETECTED"
                
                # Update hijack metrics and record results
                if hijack_detected:
                    self.update_hijack_metrics(uri, anomaly_type, current_time, 
                                              distance_from_path, data_age)
                    
                    self.record_hijack_result(uri, pos_data, distance_from_path, 
                                             data_age, hijack_detected, anomaly_type)
                    
                    hijack_drones += 1
                    if not self.hijack_detected[uri]:
                        self.hijack_detected[uri] = True
                
                self.update_controller_history(uri, controller, is_off_course, data_rate_anomaly)
                
                if is_off_course:
                    anomaly_drones += 1
                if data_rate_anomaly:
                    data_anomaly_drones += 1
                
                color = self.drone_colors.get(uri, 'black')
                anomaly_level = pos_data.get('anomaly_level', 'NORMAL')
                stale_data = pos_data.get('stale_data', False)

                # Determine marker style based on hijack status
                if hijack_detected:
                    marker = '*'
                    marker_color = 'darkred'
                    marker_size = 120
                elif stale_data:
                    marker = 'o'
                    marker_color = 'gray'
                    marker_size = 40
                elif is_off_course and data_rate_anomaly:
                    if anomaly_level == "CRITICAL":
                        marker = '^'
                        marker_color = 'darkred'
                        marker_size = 140
                    elif anomaly_level == "ALARM":
                        marker = '^' 
                        marker_color = 'red'
                        marker_size = 120
                    else:
                        marker = '^'
                        marker_color = 'orange'
                        marker_size = 100
                elif is_off_course:
                    marker = 'X'
                    marker_color = 'red'
                    marker_size = 100
                elif data_rate_anomaly:
                    if anomaly_level == "CRITICAL":
                        marker = 'D'
                        marker_color = 'darkred' 
                        marker_size = 120
                    elif anomaly_level == "ALARM":
                        marker = 'D'
                        marker_color = 'red'
                        marker_size = 100
                    else:
                        marker = 'D'
                        marker_color = 'orange'
                        marker_size = 80
                else:
                    marker = 'o'
                    marker_color = color
                    marker_size = 50
                
                self.ax.scatter(x, y, c=marker_color, s=marker_size, marker=marker,
                              edgecolors='white', linewidth=1.5, alpha=0.9,
                              label='_nolegend_')
                
                if uri not in self.drone_positions:
                    self.drone_positions[uri] = []
                self.drone_positions[uri].append((x, y))
                
                if len(self.drone_positions[uri]) > 100:
                    self.drone_positions[uri] = self.drone_positions[uri][-100:]
        
        status_text = f"Active: {active_drones} | Path Issues: {anomaly_drones} | Data Issues: {data_anomaly_drones} | Hijacked: {hijack_drones}"
        if hijack_drones > 0:
            title_color = 'darkgreen'
        elif anomaly_drones > 0 or data_anomaly_drones > 0:
            title_color = 'red'
        else:
            title_color = 'black'
        self.ax.set_title(f'Real-time Drone Hijack Monitoring\n{status_text}', 
                         fontsize=14, weight='bold', color=title_color)
        
        if hasattr(self, 'color_legend'):
            self.color_legend.remove()
            
        legend_text = "HIJACK MONITORING (BOTH DRONES):\n"
        for uri, color in self.drone_colors.items():
            drone_num = list(self.missions.keys()).index(uri) + 1
            hijack_status = "HIJACKED" if self.hijack_detected.get(uri, False) else "OK"
            legend_text += f"• {color.upper()} = Drone {drone_num} [{hijack_status}]\n"
        
        legend_text += "\nSUCCESS INDICATORS:\n"
        legend_text += "* GREEN STAR = Hijack success detected\n"
        legend_text += "• >0.3m deviation OR >3s data loss\n"
        
        self.color_legend = self.ax.text(0.02, 0.98, legend_text,
            transform=self.ax.transAxes, fontsize=8, verticalalignment='top',
            bbox=dict(boxstyle="round,pad=0.5", facecolor='lightblue', alpha=0.8))
        
        plt.draw()
        plt.pause(0.01)
        
        return active_drones, current_controllers, anomaly_drones, data_anomaly_drones, hijack_drones
    
    def complete_experiment(self):
        """Complete the experiment and generate final report."""
        print("\n" + "="*60)
        print("COMPLETING EXPERIMENT AND GENERATING REPORT")
        print("="*60)
    
        current_time = time.time()
    
        # Finalize metrics
        for uri in self.missions.keys():
            # Add any ongoing hijack time to total
            if self.hijack_detected.get(uri, False) and self.hijack_start_time.get(uri):
                ongoing_duration = current_time - self.hijack_start_time[uri]
                self.total_hijack_duration[uri] = self.total_hijack_duration.get(uri, 0) + ongoing_duration
                self.hijack_detected[uri] = False
            
            # Calculate final telemetry loss if still in data anomaly
            if self.current_data_anomaly_start.get(uri):
                self.total_telemetry_loss[uri] = current_time - self.current_data_anomaly_start[uri]
    
        # Generate summary
        self.generate_experiment_summary()
    
        # Add final messages
        self.add_message("="*50, "SYSTEM")
        self.add_message("EXPERIMENT COMPLETE", "SYSTEM")
        self.add_message(f"Total duration: {current_time - self.experiment_start_time:.1f}s", "SYSTEM")
        self.add_message(f"Report saved to: {self.results_file.replace('.csv', '_summary.txt')}", "SYSTEM")
        self.add_message("="*50, "SYSTEM")

    def run_realtime_plot(self, update_interval=0.5, duration=None):
        """
        Run the real-time plotter with experiment duration.
        
        Args:
            update_interval: Seconds between plot updates
            duration: Total experiment duration in seconds (None for indefinite)
        """
        self.add_message(f"Starting hijack experiment: {self.experiment_name}", "SYSTEM")
        if duration:
            self.add_message(f"Experiment will run for {duration} seconds", "SYSTEM")
        else:
            self.add_message("Experiment will run until Ctrl+C is pressed", "SYSTEM")
        self.add_message("Success criteria: Telemetry loss >3s OR Path deviation >0.3m", "SYSTEM")
        self.add_message("Monitoring BOTH drones for hijack symptoms", "SYSTEM")
    
        last_update = 0
        start_time = time.time()
    
        try:
            while True:
                if duration and (time.time() - start_time) > duration:
                    self.add_message(f"Experiment completed after {duration} seconds", "SYSTEM")
                    print(f"\n[EXPERIMENT] Completed after {duration} seconds")
                    break
                    
                current_time = time.time()
                if current_time - last_update >= update_interval:
                    active_count, controllers, path_anomalies, data_anomalies, hijack_count = self.update_plot()
                    last_update = current_time
                
                    if int(current_time) % 15 == 0:
                        if hijack_count > 0:
                            self.add_message(f"HIJACK STATUS: {hijack_count}/{len(self.missions)} drones hijacked", "SUCCESS")
                        else:
                            self.add_message(f"Status: {active_count} active, {path_anomalies} path issues, {data_anomalies} data issues", "INFO")
            
                time.sleep(0.1)
            
        except KeyboardInterrupt:
            print("\n\n[EXPERIMENT] Ctrl+C detected!")
            self.add_message("Experiment interrupted by user", "SYSTEM")
    
        except Exception as e:
            print(f"\n[EXPERIMENT] Error: {e}")
            self.add_message(f"Plotter error: {e}", "SYSTEM")
            import traceback
            traceback.print_exc()
    
        finally:
            # Always complete experiment and generate report
            self.complete_experiment()
        
            # Keep plot open
            plt.ioff()
            plt.show(block=True)


