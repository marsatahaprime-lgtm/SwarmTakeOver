    def init_hijack_events_csv(self):
        """Initialize CSV file for hijack events."""
        self.hijack_events_file = f"hijack_events_{datetime.now().strftime('%Y%m%d_%H%M%S')}.csv"
        with open(self.hijack_events_file, 'w', newline='') as f:
            writer = csv.writer(f)
            writer.writerow([
                'event_id', 'drone_id', 'timestamp', 'anomaly_type', 
                'experiment_time', 'distance_to_path', 'data_age',
                'max_deviation', 'telemetry_loss', 'hijack_duration',
                'success_criteria_met', 'event_type'
            ])
        self.hijack_event_counter = 0

    def record_hijack_event(self, uri, anomaly_type, current_time, event_type="DETECTION", 
                           distance_to_path=None, data_age=None):
        """
        Record hijack event to CSV.
        
        Args:
            uri: Drone URI
            anomaly_type: Type of anomaly
            current_time: Current timestamp
            event_type: Type of event (DETECTION, START, END)
            distance_to_path: Distance from path (optional)
            data_age: Age of data (optional)
        """
        drone_num = list(self.missions.keys()).index(uri) + 1
        timestamp = datetime.now().strftime('%Y-%m-%d %H:%M:%S')
        
        # Calculate success criteria
        success_criteria = 0
        if distance_to_path and distance_to_path > 1.0:
            success_criteria += 1
        if data_age and data_age > 3.0:
            success_criteria += 1
        if anomaly_type == "HIJACK_DETECTED":
            success_criteria += 1
        
        with open(self.hijack_events_file, 'a', newline='') as f:
            writer = csv.writer(f)
            writer.writerow([
                self.hijack_event_counter, drone_num, timestamp, anomaly_type,
                current_time - self.experiment_start_time, distance_to_path or 0,
                data_age or 0, self.max_path_deviation.get(uri, 0),
                self.total_telemetry_loss.get(uri, 0), 
                self.total_hijack_duration.get(uri, 0),
                success_criteria, event_type
            ])
        
        self.hijack_event_counter += 1

    def record_hijack_result(self, uri, pos_data, distance_to_path, data_age, anomaly_detected, anomaly_type):
        """
        Record detailed results for research paper.
        
        Args:
            uri: Drone URI
            pos_data: Position data dictionary
            distance_to_path: Distance from planned path
            data_age: Age of data
            anomaly_detected: Whether anomaly was detected
            anomaly_type: Type of anomaly
        """
        drone_num = list(self.missions.keys()).index(uri) + 1
        timestamp = datetime.now().strftime('%Y-%m-%d %H:%M:%S.%f')[:-3]
        
        # Calculate metrics for this drone
        hijack_duration = 0
        if self.hijack_start_time[uri] is not None:
            if self.hijack_end_time[uri] is not None:
                hijack_duration = self.hijack_end_time[uri] - self.hijack_start_time[uri]
            else:
                hijack_duration = time.time() - self.hijack_start_time[uri]
        
        # Write to CSV
        with open(self.results_file, 'a', newline='') as f:
            writer = csv.writer(f)
            writer.writerow([
                timestamp, drone_num, pos_data.get('x', 0), pos_data.get('y', 0), 
                pos_data.get('z', 0), pos_data.get('controller', 'unknown'),
                distance_to_path, data_age, int(anomaly_detected),
                anomaly_type, self.max_path_deviation[uri],
                self.total_telemetry_loss[uri],
                self.time_to_first_anomaly[uri] if self.time_to_first_anomaly[uri] else 0,
                hijack_duration
            ])
            
    def update_hijack_metrics(self, uri, anomaly_type, current_time, distance_to_path=None, data_age=None):
        """
        Update hijack metrics based on detected anomaly.
        
        Args:
            uri: Drone URI
            anomaly_type: Type of anomaly
            current_time: Current timestamp
            distance_to_path: Distance from path (optional)
            data_age: Age of data (optional)
        """
        drone_num = list(self.missions.keys()).index(uri) + 1
    
        # Initialize if not exists
        if uri not in self.total_hijack_duration:
            self.total_hijack_duration[uri] = 0
    
        # Track max path deviation
        if distance_to_path and distance_to_path > self.max_path_deviation.get(uri, 0):
            self.max_path_deviation[uri] = distance_to_path
    
        # Track telemetry loss
        if data_age and data_age > 3.0:
            if self.current_data_anomaly_start.get(uri) is None:
                self.current_data_anomaly_start[uri] = current_time - data_age
            self.total_telemetry_loss[uri] = current_time - self.current_data_anomaly_start[uri]
    
        # Track time to first anomaly
        if self.time_to_first_anomaly.get(uri) is None and anomaly_type != "NONE":
            self.time_to_first_anomaly[uri] = current_time - self.experiment_start_time
    
        # Record event to CSV
        self.record_hijack_event(uri, anomaly_type, current_time, "DETECTION", 
                               distance_to_path, data_age)
    
        # Check for hijack state changes
        if anomaly_type in ["PATH_DEVIATION", "TELEMETRY_LOSS", "HIJACK_DETECTED"]:
            if not self.hijack_detected.get(uri, False):
                # New hijack detected
                self.hijack_detected[uri] = True
                self.hijack_start_time[uri] = current_time
            
                # Generate detailed report
                report_file = self.generate_hijack_report(uri, anomaly_type, current_time, 
                                                         distance_to_path, data_age)
            
                # Record start event
                self.record_hijack_event(uri, anomaly_type, current_time, "START", 
                                       distance_to_path, data_age)
            
                print(f"\n[HIJACK START] Drone {drone_num}")
                print(f"  Type: {anomaly_type}")
                if distance_to_path:
                    print(f"  Deviation: {distance_to_path:.2f}m")
                if data_age:
                    print(f"  Data age: {data_age:.1f}s")
                print(f"  Report: {report_file}")
        
            else:
                # Continuing hijack - accumulate time
                if self.hijack_start_time.get(uri):
                    elapsed = current_time - self.hijack_start_time[uri]
                    self.hijack_start_time[uri] = current_time
                    self.total_hijack_duration[uri] += elapsed
    
        elif anomaly_type == "NONE" and self.hijack_detected.get(uri, False):
            # Hijack ended
            if self.hijack_start_time.get(uri):
                final_elapsed = current_time - self.hijack_start_time[uri]
                self.total_hijack_duration[uri] += final_elapsed
        
            # Record end event
            self.record_hijack_event(uri, "NONE", current_time, "END", 
                                   distance_to_path, data_age)
        
            self.hijack_detected[uri] = False
            self.hijack_start_time[uri] = None
        
            print(f"\n[HIJACK END] Drone {drone_num}")
            print(f"  Total duration: {self.total_hijack_duration.get(uri, 0):.1f}s")
            print(f"  Max deviation: {self.max_path_deviation.get(uri, 0):.2f}m")
            print(f"  Telemetry loss: {self.total_telemetry_loss.get(uri, 0):.1f}s")
    
